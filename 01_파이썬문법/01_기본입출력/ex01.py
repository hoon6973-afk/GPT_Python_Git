# 문자열 연결 연산자 : +
a = "파이썬"
b = "프로그래밍"
print(a + " " + b)

# str() : 문자열 변환 함수
num = 10
print("숫자 : " + str(num))

#문자열 반복 연산자 : *
print("여러 번 반복하여 출력\n" * 10)
