# ctrl + F5 : 파이썬 실행
# shift = enter : 한줄 실행
print("안녕하세요")
print("파이썬 \n 프로그래밍")
print("\"아이스아메리카노\"가 주문되었습니다.")

#alt + shift + ↓ : 한줄복사
print("이름\t\t: 김조은")
print("성별\t\t: 남자")
print("키\t\t: 180")
print("생년월일\t\t: 2025.09.13")

# 여러줄 문자열
